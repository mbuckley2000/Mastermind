/**
 * A 2-Dimensional vector class for integers
 * The X and Y value of the vector can be publicly accessed
 *
 * @author mb2070
 * @since 24/12/2015
 */

public class Vector2i {
	public int x;
	public int y;

	/**
	 * @param x X value of vector
	 * @param y Y value of vector
	 */
	public Vector2i(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
